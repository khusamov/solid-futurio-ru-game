import {IQueue} from './IQueue';

/**
 * Общий интерфейс команд.
 *
 * У каждой команды могут быть определены дополнительные методы,
 * которые могут менять состояние команды (например inject()).
 */
export interface ICommand {
	/**
	 * Выполнить команду.
	 * Все параметры должны передаваться через конструктор команды или замыкание.
	 */
	execute(): void

	/**
	 * Ссылка на очередь команд.
	 * Определяется непосредственно перед помещением команды в очередь команд.
	 */
	commandQueue?: IQueue<ICommand>

	/**
	 * Имя команды.
	 * Используется в отладочных целях.
	 */
	readonly name: string

	// TODO Может стоить добавить description? Чтобы name содержало идентификатор, а все пояснения были в description.
	// TODO Похоже надо удалить name. Всю инфу можно получить при помощи console.log(command).
}